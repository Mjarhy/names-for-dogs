const itemsPerPage = 10;
let currentPage = 1;
let allNames = [];

function generateNames(gender, language, theme) {
  const genderData = namesData[gender];
  if (!genderData) return [];
  const languageData = genderData[language];
  if (!languageData) return [];
  const themeData = languageData[theme];
    if (!themeData) return [];
  return themeData || [];
}

function displayNames(names) {
  const resultContainer = document.getElementById("result");
  resultContainer.innerHTML = "";
  if (names.length === 0) {
    resultContainer.innerHTML = "<p>لا توجد أسماء متاحة للخيارات المحددة.</p>";
    return;
  }

  const list = document.createElement("ul");
    names.forEach((item) => {
      const listItem = document.createElement("li");
      listItem.innerHTML = `<b>${item.name}</b> <span>- ${item.meaning}</span>`;
      list.appendChild(listItem);
    });
    resultContainer.appendChild(list);
  }

function displayPagination() {
  const paginationContainer = document.querySelector(".pagination-controls");
  const pageNumbersContainer = document.getElementById("pageNumbers");

  pageNumbersContainer.innerHTML = "";

  const totalPages = Math.ceil(allNames.length / itemsPerPage);

   if (totalPages > 0) {
      paginationContainer.style.display = "flex";
      for (let i = 1; i <= totalPages; i++) {
        const pageNumberButton = document.createElement("button");
        pageNumberButton.textContent = i;
        pageNumberButton.classList.add("page-number");
        if (i === currentPage) {
          pageNumberButton.classList.add("active");
        }
        pageNumberButton.addEventListener("click", () => {
          currentPage = i;
          displayCurrentPage();
          updatePaginationButtons(totalPages);
        });
        pageNumbersContainer.appendChild(pageNumberButton);
      }
  } else {
      paginationContainer.style.display = "none";
  }

  updatePaginationButtons(totalPages);
}

function updatePaginationButtons(totalPages) {
  const prevButton = document.getElementById("prevPageBtn");
  const nextButton = document.getElementById("nextPageBtn");

  prevButton.disabled = currentPage === 1;
  nextButton.disabled = currentPage === totalPages;
}

function displayCurrentPage() {
    const startIndex = (currentPage - 1) * itemsPerPage;
    const endIndex = startIndex + itemsPerPage;
    const currentPageNames = allNames.slice(startIndex, endIndex);
    displayNames(currentPageNames);
  }

document.getElementById("generateBtn").addEventListener("click", function () {
  const gender = document.getElementById("gender").value;
  const language = document.getElementById("language").value;
  const theme = document.getElementById("category").value;
  allNames = generateNames(gender, language, theme);
  currentPage = 1;
  displayCurrentPage();
  displayPagination();
});

document.getElementById("prevPageBtn").addEventListener("click", () => {
  if (currentPage > 1) {
    currentPage--;
    displayCurrentPage();
    displayPagination();
  }
});

document.getElementById("nextPageBtn").addEventListener("click", () => {
  const totalPages = Math.ceil(allNames.length / itemsPerPage);
  if (currentPage < totalPages) {
    currentPage++;
    displayCurrentPage();
    displayPagination();
  }
});
